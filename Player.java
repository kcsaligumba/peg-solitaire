public class Player
{
    private String symbol;
    
    /**
     * Stores symbol choice
     * @param symbol the symbol 
     */
    public void setSymbol(String symbol)
    {
        this.symbol = symbol;
    }
    
    /**
     * Gets the symbol stored
     * @return the symbol
     */
    public String getSymbol()
    {
        return symbol;
    }
}